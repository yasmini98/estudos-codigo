console.log("==== ARITMETICA====");
console.log(2+5); //somar
console.log(2-5); //subtrair
console.log(2*5); //multiplicar
console.log(2/5); //dividir
console.log(2%5); //resto

console.log(Math.abs(-5)); //valor absoluto é sempre positivo
console.log(Math.ceil(5.2)); //arredondar para cima
console.log(Math.floor(10.6)); //arredondar para baixo
console.log(Math.round(10.8)); //arredondar para o mais próximo

console.log(Math.max(1,2,3,4,5)); //maior número
console.log(Math.min(1,2,3,4,5)); //menor número

console.log(Math.random()); //número aleatório entre 0 e 1

console.log(Math.trunc(5.5)); //retorna a parte inteira do número